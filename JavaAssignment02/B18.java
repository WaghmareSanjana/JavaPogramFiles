/*Question 18 :Write a program to print following :
**********
**********
**********
**********
*/


class B18 {

  public static void main(String[] args) {
    int r=4,c;

    for (r = 1; r<=4; r++) {
      for (c = 1; c <= 4;c++) {
        System.out.print("* ");
      }
      System.out.println();
    }
  }
}
